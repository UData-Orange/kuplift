def test_input_variables(opt_ue_with_catvar):
    assert opt_ue_with_catvar.input_variables == ["VAR1", "VAR2", "VAR3", "VAR4"]
    assert opt_ue_with_catvar.informative_input_variables == ["VAR1", "VAR2", "VAR3"]
